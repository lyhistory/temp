        <p><p class="cj-1-p"><br>91投房，房产众筹怎么玩？</p><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        做中国的Fundrise
        2013年上线的Fundrise是一家专门从事房地产投资的众筹平台，它的出现特奠定了美国众筹地产的基础。而91投房则是将Fundrise商业模式做出本土化改良，解决地产开发中资金链问题，并通过互联网缩短开发商与实际购房、投资人的中间环节，减少因此造成的资金成本。
        91投房成立于203年9月份，网站于2014年6月27日正式上线，与国内现存几家地产众筹网站不同，91投房成立之初的目标就是为中国房地产向智能有序化发展提供可行化解决方案。
        房地产众筹的概念最初在2013年出现于美国，2014年开始进入中国。91投房不同于传统的互联网金融平台，它采用创新的物权众筹众智模式，桥接项目开发方和终端消费者。它可以帮助传统开发商充分拥抱互联网，完善升级其旧有的开发模式；也帮助终端用户拓展新的理财渠道：比如说，生活在城市中的白领和刚刚毕业的大学生能够通过平台找到属于自己的家，即将退休和已经退休人士找到最适合的养老地产，热爱旅游的驴友们发现中意的旅游地产。这样在降低融资成本的同时也提高了闲散资金的回报率，从而通过优化整体行业的资源配置达到三赢的效果。
        房地产众筹可以有效解决地产融资中由于国家和银行政策频繁变更造成的资金链断裂问题，并扩宽融资渠道，使得项目发展更为平滑，同时为公众提供了一个新的低风险投资理财方式。由于投资者基于对地方市场的理解，使得主动选择下的获投项目自然更易得到其他投资者的认可，使得市场资金配置更为有效。目前最适合在众筹平台上融资的是旅游地产、养老地产以及社区商业等有可预期现金流，并广为公众所熟悉的项目。客户不仅可以在所投项目上获得资本升值收入，同时还可以对所有物权进行使用消费，使得投资更加物有所值。
