            91 投房团队在房地产领域颇有耕耘，其团队主要成员相互在新加坡求学时认识，在校期间就联合学校教授，一起做过东南亚的旅游地产项目，由他们团队负责在印尼等旅游目的地选择地产，合作的新加坡房产中介负责销售，共完成了约合 3000 万人民币的销售。
            团队回国后，经历了很多的调研和试错，最初他们仍然想做旅游地产的项目，例如丽江的客栈项目等，但是目前该类项目过剩，并且大多数处在三四线城市，未来风险较大。他们也研究过养老地产，养老地产对于医护的要求高，一旦老人身体出现问题，容易产生纠纷，市场也不成熟。最终在出租公寓和联合办公空间的选择中，他们选择了公寓，因为目前联合办公项目也已经趋于饱和，未来一些项目有亏损风险，而公寓市场仍然供不应求。
            未来 91 投房希望向整个产业链进行延伸，对标的是美国 EQR 公司，向上帮助公寓运营公司寻找优质房源和项目，向下为其吸引客户，同时提供融资的服务。91 投房非常重视数据的分析，包括项目所在地区周边的产业结构、周围人群分布、附近居民平均工资等等，最终综合判断项目的盈利前景，目前公司每月会出具一份地产行业报告。
            91 投房创始人王建翔本科就读于哈工大，硕士毕业于新加坡国立大学，曾供职于浦发银行，负责风险管理工作，是连续创业者。CTO 刘跃是厦门大学计算机学士、新加坡国立大学计算机硕士，原新加坡星展银行高级构架师。目前 91 投房获 58 同城 CEO 姚劲波、36 氪 CEO 刘成城、知名天使投资人戴志康等人投资。
