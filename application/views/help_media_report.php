            REITs (Real Estate Investment Trusts)，即房地产信托投资基金，通过金融手段把流动性较低的房地产投资,直接转化为资本市场上的证券资产进行交易,其收益主要来自于租金收益和房地产升值。然而,受困于国内的法律和税务环境,REITs 迟迟未能问世。
            91 投房基于 REITs 的方式，设有股权以及债权两类产品。股权类的产品一般预期三年可以收回成本，之后每年有 30%甚至更高的收益，一般合同规定，8-10年 后项目到期，股权将由品牌公寓运营商原价回购。另外一类是债券类的项目，提供年化 15%-20%的受益，风控方面 91 投房设置了三个基本要求，合作方至少有一个成功运营的项目，入住率超过 95%；月净流水要超过 50 万元；公寓服务方融资金额要超过 1000 万人民币。
